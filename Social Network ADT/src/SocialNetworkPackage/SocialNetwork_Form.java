    private void createAccMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccMenuItemActionPerformed
        // TODO add your handling code here:
        String accountName = JOptionPane.showInputDialog("What will your account name be?");
        if (accountName.isEmpty())
            JOptionPane.showMessageDialog(null, "Account not created. No name was input.");
        else
        {
            handler.createAccount(accountName);
            JOptionPane.showMessageDialog(null, "Account Created! You're now logged in!");
            currentProfile = handler.getProfile(accountName);
            if(currentProfile == null)
                JOptionPane.showMessageDialog(null, "Account not Found");
            else
            {
                displayProfile();
                
                loginMenuItem.setEnabled(false);
                createAccMenuItem.setEnabled(false);
                deleteAccMenuItem.setEnabled(true);
                createProfMenuItem.setEnabled(true);
                modifyProfMenuItem.setEnabled(true);
                searchfrMenuItem.setEnabled(true);
                logoutButton.setEnabled(true);
            }
            
        }
    }//GEN-LAST:event_createAccMenuItemActionPerformed

