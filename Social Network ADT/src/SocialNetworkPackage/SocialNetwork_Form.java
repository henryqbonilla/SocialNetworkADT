    private void searchfrMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchfrMenuItemActionPerformed
        // TODO add your handling code here:
        String nameSearch = JOptionPane.showInputDialog("Type in name of friend you would like to search for");
        Profile p = handler.getProfile(nameSearch);
        if (p == null)
            JOptionPane.showMessageDialog(null, "Sorry! Friend not Found!");
        else
        {
            String message = p.getName() + " was Found!";
            JOptionPane.showMessageDialog(null, message);
            int decision = JOptionPane.showConfirmDialog(null, "Would you like to add " + 
                    p.getName() + " as a friend?");
            
            if(decision == 2)
            {
                //do nothing
            }
            else if(decision == 1)
            {
                JOptionPane.showMessageDialog(null, "Friend not added.");
            }
            else
            {
                currentProfile.addFriend(p.getName());
                p.addFriend(currentProfile.getName());
                displayProfile();
            }
            
            
        }
    }//GEN-LAST:event_searchfrMenuItemActionPerformed

