    private void displayProfile()
    {   
        nameText.setText(currentProfile.getName());
        statusTextField.setText(currentProfile.getStatus());
        
        ArrayList<String> s = currentProfile.getFriends();
        if (s.size() == 0)
            friendsListArea.setText("no friends");
        else
        {
            StringBuilder sList = new StringBuilder();
            for (int i = 0; i < s.size(); i++)
            {
                sList.append(s.get(i));
                sList.append("\n");
            }
            friendsListArea.setText(sList.toString());
        }
        
    }
    
